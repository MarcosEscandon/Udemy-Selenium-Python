string = "Te quiero solo como amigo"

print(string[0:2]) # Imprime los dos primeros caracteres
print(string[-3:]) # Imprime los ultimos tres caracteres
print(string[::2]) # Imprimir cadena de 2 en 2
print(string[::-1]) # Imprimir cadena en sentido inverso
print(string+string[::-1]) # Imprime cadena espejada